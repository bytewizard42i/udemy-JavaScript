//* Number Challenge

let x = Math.floor(Math.random() * 100) + 1;

let y = Math.floor(Math.random() * 50) + 1;

console.log('\n');

//*---------------------------------*/

let randomSum = x + y;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '+', 'Random number from 1-50 =', y, '=', randomSum);    

console.log('\n');

//*---------------------------------*/

let randomDifference = x - y;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '-', 'Random number from 1-50 =', y, '=', randomDifference);

console.log('\n');

//*---------------------------------*/

let randomProduct = x * y;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '*', 'Random number from 1-50 =', y, '=', randomProduct);

console.log('\n');

//*---------------------------------*/

let randomQuotient = x / y;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '/', 'Random number from 1-50 =', y, '=', randomQuotient);

console.log('\n');

//*---------------------------------*/

let randomModulus = x % y;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '%', 'Random number from 1-50 =', y, '=', randomModulus);

console.log('\n');

//*---------------------------------*/

let randomExponent = x ** y;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '**', 'Random number from 1-50 =', y, '=', randomExponent);

console.log('\n');

//*---------------------------------*/

let randomIncrement = x++;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '++', '=', randomIncrement);

console.log('\n');

//*---------------------------------*/

let randomDecrement = x--;

console.log('The solution to the number challenge is: Random number x from 1-100 =', x, '--', '=', randomDecrement);

